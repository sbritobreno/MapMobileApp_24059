function DefineMarkerColor(id) {
    if (id === 1) {
        return '#100'
    } else if (id === 2) {
        return '#010'
    } else if (id === 3) {
        return '#001'
    } else if (id === 4) {
        return '#011'
    } else if (id === 5) {
        return '#110'
    } else if (id === 6) {
        return '#400'
    } else if (id === 7) {
        return '#004'
    } else if (id === 8) {
        return '#040'
    } else if (id === 9) {
        return '#440'
    } else if (id === 10) {
        return '#044'
    } else if (id === 11) {
        return '#800'
    } else if (id === 12) {
        return '#008'
    } else if (id === 13) {
        return '#080'
    } else if (id === 14) {
        return '#088'
    } else {
        return '#880'
    }
}

export default DefineMarkerColor;